#include <stdio.h>
int main()
{
	float s, s1 = 0, s2 = 0;//s1和s2分别存放正项数列和负项数列的累加和
	int i;
	for (i = 1; i < 101; i += 2)
	{
		s1 += 1. / i;      //累加正项（奇数项）到s1
		s2 += 1. / (i + 1);//累加负项（偶数项）到s2，在奇数的基础上加一个即偶数，然后循环回去偶数再加
	}
	
	
	s = s1 - s2;  //减即表示负   //s1-s2得到最终的数列累加和s

printf("s=%f\n", s);
return 0;

}
