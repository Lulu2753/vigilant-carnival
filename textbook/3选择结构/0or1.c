#include <stdio.h>
#include <math.h>

int main()
{
	printf("%d", (9 > 5)>3); //这个不等式得到的结果为真假值1或0
	printf("\n%d\n", 9 > 5 && 5 > 3); //连等式得分开写，用&&隔开

	int a, b, c = 999;
	a =!(b =1 && !-5) || (c = 0 && 6);  //优先级||<&&<!,非到且到或，！表示对真值表达式取反，也可以对数字取反，负数取反也是0，即非零数取反为0
	printf("a=%d,b=%d,c=%d\n", a, b, c); //||同假为假，&&同真为真
	

	return 0;
}